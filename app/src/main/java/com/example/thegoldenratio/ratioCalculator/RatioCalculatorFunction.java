package com.example.thegoldenratio.ratioCalculator;

public class RatioCalculatorFunction {

    //Overloading
    public Double calculateA(Double B){
        return B/1.618;
    }

    public Double calculateA(){
        return -1.0;
    }

    public Double calculateAplusB(Double A, Double B){
        return A + B;
    }
}
